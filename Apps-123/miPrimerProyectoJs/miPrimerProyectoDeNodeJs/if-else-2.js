// If else + if else

let nota = 5;

if (nota === 5) {
  console.log("Excelente");
} else if (nota === 4) {
  console.log("Muy bueno");
} else if (nota === 3) {
  console.log("Bueno");
} else if (nota === 2) {
  console.log("Regular");
} else if (nota === 1) {
  console.log("Malo");
} else {
  console.log("Error");
}
